package telusko;

public class IterationDemo {

	public static void main(String[] args) {
		
		int i=8; //initialization
		while(i<=5)//condition
		{
		System.out.println("hello");
		i++; //iteration
		}
		
		int j=0; //initialization
		do
		{
		System.out.println("world");
		j++; //iteration
		} while(j<=5); //condition
		
		for(int k=1;k<=5;k++) {
			System.out.println("forloop");
		}

	}
}
